%index
hsptv_getrank
HSPTVデータ取得

%prm
p1, p2, p3, rank
p1 : スコア情報が代入される変数名
p2 : ユーザー名情報が代入される変数名
p3 : コメント情報が代入される変数名
rank : 順位のインデックス(0〜29)

%inst
HSPTVデータの内容を変数に読み出します。

rankの値は0が1位、29が30位となります。p1は数値型、p2＆p3は文字列型として初期化されます。

この命令は、hsptv_up命令によって更新されたデータを変数に読みだすものです。読み出される内容は、最後にhsptv_up命令を実行した時点の情報になります。
また、この命令では通信は発生しないので、情報の読み出しは即時に行なわれます。

